import os{get_line}
fn main() {
	n := get_line().split(' ').map(it.u16())[0]
  if n == 1 {
      println("Hello World")    
  } else {
      a := get_line().split(' ').map(it.u16())[0]
      b := get_line().split(' ').map(it.u16())[0]
      println(a+b)       
    }
}
