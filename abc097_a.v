import os { get_line }

fn abs(x int) int {
    if x < 0 {
        return -x
    }
    return x
}

fn main() {
    input := get_line().split(' ').map(it.int())
    a := input[0]
    b := input[1]
    c := input[2]
    d := input[3]

    if abs(c - a) >= 0 && abs(c - a) <= d {
        println('Yes')
    } else if abs(a - b) >= 0 && abs(a - b) <= d && abs(b - c) >= 0 && abs(b - c) <= d {
        println('Yes')
    } else {
        println('No')
    }
}
