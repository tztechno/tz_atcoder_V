import os { get_line }

fn main() {
    input := get_line().split(' ').map(it.int())
    a, b := input[0], input[1]
    s := [a + b, a - b, a * b]
    mut mx := s[0]
    for i in 1 .. 3 {
        if s[i] > mx {
            mx = s[i]
        }
    }
    println(mx)
}
